package com.tekwill.learning.basics;

public class GiantLetters {
    public static void main(String[] args){
        System.out.println("PPPPPP     RRRRRR        OOO        CCC        A");
        System.out.println("P     P    R     R      O   O      C   C      A A");
        System.out.println("P     P    R     R     O     O    C           A A");
        System.out.println("PPPPPP     RRRRRR      O     O    C          A   A");
        System.out.println("P          R R         O     O    C          AAAAA");
        System.out.println("P          R   R        O   O      C   C    A     A");
        System.out.println("P          R     R       OOO        CCC    A       A");
    }
}
